package Math;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.abs(88));
        System.out.println(Math.abs(-88));
        System.out.println("--------");
        System.out.println(Math.ceil(12.35));
        System.out.println(Math.ceil(12.31));
        System.out.println("--------");
        System.out.println(Math.floor(12.34));
        System.out.println(Math.floor(12.75));
        System.out.println("--------");
        System.out.println(Math.round(12.34F));
        System.out.println(Math.round(12.75F));
        System.out.println("--------");
        System.out.println(Math.max(66,99));
        System.out.println("--------");
        System.out.println(Math.pow(2.5,3.9));
        System.out.println("--------");
        System.out.println((int)(Math.random()*100)+1);


    }
}
